// Seed function
export async function seedAgentCatalog() {
  console.log("[AgentCatalog] Starting seed...");
  
  try {
    // Insert categories
    console.log("[AgentCatalog] Inserting categories...");
    const insertedCategories = await db.insert(agentCategories).values(categories).returning();
    console.log(`[AgentCatalog] Inserted ${insertedCategories.length} categories`);
    
    // Create category lookup
    const categoryMap = new Map(insertedCategories.map(c => [c.slug, c.id]));
    
    // Map templates to include category IDs
    const templatesWithCategories = templates.map(t => ({
      ...t,
      categoryId: categoryMap.get(t.domain) || null,
    }));
    
    // Insert templates in batches
    console.log("[AgentCatalog] Inserting templates...");
    const batchSize = 20;
    let totalInserted = 0;
    
    for (let i = 0; i < templatesWithCategories.length; i += batchSize) {
      const batch = templatesWithCategories.slice(i, i + batchSize);
      await db.insert(agentTemplates).values(batch);
      totalInserted += batch.length;
      console.log(`[AgentCatalog] Inserted ${totalInserted}/${templatesWithCategories.length} templates`);
    }
    
    console.log("[AgentCatalog] Seed complete!");
    return { categories: insertedCategories.length, templates: totalInserted };
  } catch (error) {
    console.error("[AgentCatalog] Seed error:", error);
    throw error;
  }
}

// Check if already seeded
export async function isAgentCatalogSeeded(): Promise<boolean> {
  const result = await db.select().from(agentCategories).limit(1);
  return result.length > 0;
}
